import java.util.Scanner;
class For {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter number of elements: ");
        int n = sc.nextInt();
        sc.close();
        for(int i=1;i>=10;i--){
            
            System.out.println(n+"*"+i+"="+n*i);
            

        
        }}
}