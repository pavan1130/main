class RelationOperator {
    public static void main(String[]args){
        int a =50;
        int b=100;
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(a|b);    
    }
    
}
